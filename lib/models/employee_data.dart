class EmployeeData {
  final String? alamat;
  final String? divisi;
  final String? email;
  final String? jabatan;
  final String? jenisKelamin;
  final String? name;
  final String? nip;
  final String? numberPhone;
  final String? password;
  final String? sisaCuti;
  final String? tanggalLahir;
  final String? uid;

  EmployeeData({
