    required this.divisi,
    required this.email,
    required this.jabatan,
    required this.jenisKelamin,
    required this.name,
    required this.nip,
    required this.numberPhone,
    required this.password,
    required this.sisaCuti,
    required this.tanggalLahir,
    required this.uid,
  });
}
